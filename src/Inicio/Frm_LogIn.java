    private void bntLogInActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_Btn_incActionPerformed
        String username = textFieldUsername.getText().trim();
        String password = new String(textFieldPassword.getPassword()).trim();

        if (username.isEmpty() || password.isEmpty()) {
            labelMessage.setText("Por favor, llene todos los campos");
            return;
        }

        UsuarioController usuarioController = new UsuarioController();

        try {
            if (usuarioController.validarUsuario(username, password)) {
                new Frm_PaginaInicio(new Usuario(username, password)).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Las credenciales son incorrectas", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al validar usuario", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }// GEN-LAST:event_Btn_incActionPerformed

